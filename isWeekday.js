export default function isWeekday(Weekdays){
    if (Weekdays == 'Monday'){
    return true
    }else if(Weekdays == 'Tuesday'){
    return true
    }else if (Weekdays == 'Wednesday'){
    return true
    }else if (Weekdays == 'Thursday'){
    return true
    }else if (Weekdays == 'Friday'){
    return true
    }else{
    return false
    }
  }